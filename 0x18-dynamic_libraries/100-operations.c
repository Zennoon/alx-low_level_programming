/**
 * add - Returns the sum of two integers
 * @a: First operand
 * @b: Second operand
 *
 * Return: a + b
 */
int add(int a, int b)
{
	return (a + b);
}

/**
 * sub - Returns the difference between two integers
 * @a: First Operand
 * @b: Second operand
 *
 * Return: a - b
 */
int sub(int a, int b)
{
	return (a - b);
}

/**
 * mul - Returns the product of two integers
 * @a: First operand
 * @b: Second operand
 *
 * Return: a * b
 */
int mul(int a, int b)
{
	return (a * b);
}

/**
 * div - Returns the quotient of two integers
 * @a: First operand (Dividend)
 * @b: Second operand (Divisor)
 *
 * Return: a / b
 */
int div(int a, int b)
{
	return (a / b);
}

/**
 * mod - Returns the modulo of two integers
 * @a: First operand (Dividend)
 * @b: Second operand (Divisor)
 *
 * Return: a % b
 */
int mod(int a, int b)
{
	return (a % b);
}
